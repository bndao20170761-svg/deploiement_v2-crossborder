export function getTranslation(key, language = "fr"||"en"||"pt") {
  const keys = key.split(".");
  let value = translations;

  for (const k of keys) {
    value = value?.[k];
    if (!value) return key; // clé introuvable → fallback
  }

  // Si c’est un objet avec fr/en/pt → on retourne la bonne langue
  if (value?.[language]) {
    return value[language];
  }

  return typeof value === "string" ? value : key; // fallback
}
